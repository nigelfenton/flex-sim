class Radio:
    def __init__(self, ip, ae_ip, pattern="ramp", bins=BINS, fps=FPS, width_khz=SIGNAL_WIDTH_KHZ,
                 port=DEFAULT_PORT, radio_id=0, model=MODEL):
        self.ip, self.ae_ip = ip, ae_ip
        self.port = port                # control/data port we bind + advertise (discovery dest stays 4992)
        self.model = model if model in MODELS else MODEL
        self.max_slices = MODELS.get(self.model, {}).get("slices", 4)   # advertised cap (multi-slice TODO)
        # Per-radio identity, so many radios can live in one process (the "rack").
        # Each value must be unique per radio or AE cross-wires them; derived from
        # radio_id off the base constants below.
        self.radio_id = radio_id
        self.serial = f"FLEXSIM{radio_id:02d}"
        self.client_handle = HANDLE + radio_id   # NB: NOT self.handle — that's the method!
        self.handle_hex = f"{self.client_handle:08X}"
        self.pan_id = PAN_ID + radio_id * 16     # FFT stream-id BASE; AE stacks a panadapter per +RX,
        self.wf_id = WF_ID + radio_id * 16       # waterfall base. pan k -> base+k (16 ids/radio, no overlap)
        self.pans = {}                           # pan_id -> {"wf_id":int,"slice":global_idx|None,"y_pixels":int}
        self.pan_seq = 0                          # next panadapter index for this radio
        self.meter_sid = METER_SID + radio_id    # meter VITA stream id (PCC 0x8002)
        self.pattern = pattern if pattern in PATTERNS else "ramp"
        self.bins, self.fps = bins, fps
        self.sig_width_khz = width_khz
        self.noise_floor_dbm = -120.0   # live (control panel, dBm; ~S1)
        self.sig_level_dbm = S9_DBM     # live (control panel, dBm; default S9 = -73)
        self.noise_color = 0.0          # live (noise tilt: 0=white, >0=pink)
        self.noise_cal_full = True      # live: noise_cal full-span (standard) vs width-limited
        self.vita_dest = None
        self.run = True
        self.send_lock = threading.Lock()   # serialize TCP writes: stream thread (status) vs command thread (replies)
        self.streaming = False
        self.paused = False             # Stop/Go: when True, stay connected but send NO pan/wf/meter data
                                        # (AE keeps the radio; panadapter + waterfall go dead). Go resumes.
        self.ae_peer_ip = None          # only accept the UDP prime from the connected AE
        self.y_pixels, self.min_dbm, self.max_dbm = 700, -130.0, -20.0
        self.center_mhz, self.span_mhz = CENTER_MHZ, SPAN_MHZ   # track AE's actual pan view
        self.slice_freq = CENTER_MHZ                            # active slice freq (= VFO); mirrors slices[active]
        self.slice_mode = "USB"
        self.slices = {}            # index -> {"freq":MHz,"mode":str,"active":bool}; AE adds via +RX (slice create)
        self.active_slice = 0       # the slice the pan/waterfall/primary carrier follow
        self.conn = None            # active TCP conn (so the control panel can push TX status)
        self.tx_mox = False         # live: TX keyed (panel toggle or AE's 'transmit set mox=1')
        self.tx_tune = False
        self.tx_power_w = 100.0      # live: forward power (W) reported while TX
        self.tx_swr = 1.2           # live: SWR reported while TX
        self.cwx_wpm = 20           # CWX keyer speed (AE drives via 'cwx wpm')
        self.cwx_active = False      # an AE-initiated CWX transmission is in progress
        self.cwx_schedule = []
        self.cwx_dur = 0.0
        self.cwx_t0 = 0.0
        self.cwx_char_ends = []
        self.cwx_sent = 0
        self.qsk = False             # full break-in (RX between elements); AE sets via cwx qsk_enabled
        self.enabled = True          # "power": when False, stop advertising (radio drops off AE)
        self.last_vfo_dbm = -130.0   # last level at the VFO — drives the rack strip's signal meter
        # remote_audio_rx stream state
        self.audio_stream_id = None
        self.audio_stop = threading.Event()
        self.audio_thread = None
        self.audio_tone_hz = 440.0 + radio_id * 220.0  # distinct tone per radio (A4, C#5, …)
        self.audio_reduced_bw = False  # set True when AE sends 'client set send_reduced_bw_dax=1'
        self.audio_source   = AUDIO_SRC_TONE  # AUDIO_SRC_TONE | AUDIO_SRC_NOISE | AUDIO_SRC_WAV
        self.audio_wav_path = None             # filesystem path to WAV file (used when source=wav)
        # dax_rx stream state (RADE / digital decode path). AE arms this via
        # 'stream create type=dax_rx dax_channel=N' when a slice goes RADE/DIGU;
        # we register an id back so AE feeds these packets to its host-side decoder.
        self.dax_channel = None                # the dax_channel AE requested (1..4), or None

    @property
    def tx_on(self):
        return self.tx_mox or self.tx_tune

    def set_power(self, on):
        # Rack "power button": off -> stop streaming + drop AE's connection + stop
        # advertising (radio vanishes from AE). on -> discovery resumes, AE re-finds it.
        self.enabled = bool(on)
        if not on:
            self.streaming = False
            c = self.conn
            if c:
                try: c.shutdown(socket.SHUT_RDWR); c.close()
                except OSError: pass

    def state(self):
        # Snapshot for the rack strip (polled by the panel).
        return {"id": self.radio_id, "serial": self.serial, "model": self.model, "ip": self.ip,
                "on": self.enabled, "connected": self.conn is not None,
                "freq": round(self.slice_freq, 5), "mode": self.slice_mode, "tx": self.tx_on,
                "pattern": self.pattern, "power_w": round(self.tx_power_w),
                "meter_dbm": round(self.last_vfo_dbm, 1),
                "slices": len(self.slices), "max_slices": self.max_slices}

    def dbm_to_pixel(self, dbm):
        p = (self.max_dbm - dbm) / (self.max_dbm - self.min_dbm) * (self.y_pixels - 1)
        return max(0, min(self.y_pixels - 1, int(round(p))))

    def dbm_to_wf_raw(self, dbm):
        frac = max(0.0, min(1.0, (dbm - self.min_dbm) / (self.max_dbm - self.min_dbm)))
        return int(round((WF_FLOOR_VAL + frac * (WF_PEAK_VAL - WF_FLOOR_VAL)) * 128))

    def discovery_loop(self):
        s = socket.socket(socket.AF_INET, socket.SOCK_DGRAM)
        s.setsockopt(socket.SOL_SOCKET, socket.SO_BROADCAST, 1)
        dests = [("255.255.255.255", DISCOVERY_PORT)] + ([(self.ae_ip, DISCOVERY_PORT)] if self.ae_ip else [])
        while self.run:
            if self.enabled:                       # "powered off" -> stop advertising
                msg = (f"name=flex-sim model={self.model} serial={self.serial} version={VERSION} "
                       f"ip={self.ip} port={self.port} status=Available mf_enable=1 "
                       f"slices={self.max_slices} pans={self.max_slices} "   # capacity -> AE enables +RX
                       f"max_licensed_version=3").encode()   # rebuilt each tick -> live model change propagates
                for d in dests:
                    try: s.sendto(msg, d)
                    except OSError: pass
            time.sleep(1.0)

    def prime_loop(self):
        s = socket.socket(socket.AF_INET, socket.SOCK_DGRAM)
        s.setsockopt(socket.SOL_SOCKET, socket.SO_REUSEADDR, 1)
        s.bind((self.ip, self.port))
        while self.run:
            try:
                data, addr = s.recvfrom(2048)
            except OSError:
                break
            if self.ae_peer_ip and addr[0] == self.ae_peer_ip and not self.vita_dest:
                self.vita_dest = addr
                log(f"[udp] captured VITA dest from prime: {addr} ({len(data)}B)")

    def serve(self):
        srv = socket.socket(socket.AF_INET, socket.SOCK_STREAM)
        srv.setsockopt(socket.SOL_SOCKET, socket.SO_REUSEADDR, 1)
        srv.bind((self.ip, self.port)); srv.listen(1)   # bind our own IP (rack: each radio a distinct IP)
        log(f"[tcp] listening on :{self.port}  (radio ip {self.ip}, pattern={self.pattern})")
        while self.run:
            conn, addr = srv.accept()
            log(f"[tcp] AE connected from {addr}")
            self.ae_peer_ip = addr[0]; self.vita_dest = None
            try: self.handle(conn)
            except Exception as e: log("[tcp] conn error:", e)
            finally:
                conn.close()
                self.streaming = False; self.ae_peer_ip = None; self.conn = None; self.vita_dest = None
                self.audio_stop.set(); self.audio_stream_id = None; self.dax_channel = None
                # Free this client's receivers/panadapters on disconnect, like a real radio.
                # Without it, stale slices/pans pile up across reconnects and the slice
                # lettering drifts (A -> B -> C on each new connection).
                self.slices.clear(); self.pans.clear(); self.pan_seq = 0; self.active_slice = 0

    def handle(self, conn):
        self.conn = conn
        with self.send_lock:
            conn.sendall(f"V{VERSION}\n".encode())
            conn.sendall(f"H{self.handle_hex}\n".encode())
        log(f"[tcp] sent V{VERSION} / H{self.handle_hex}")
        buf = b""
        while self.run:
            chunk = conn.recv(4096)
            if not chunk: break
            buf += chunk
            while b"\n" in buf:
                line, buf = buf.split(b"\n", 1)
                line = line.decode(errors="replace").strip()
                if line: self.on_line(conn, line)

    def reply(self, conn, seq, body="", code=0):
        with self.send_lock:
            conn.sendall(f"R{seq}|{code:X}|{body}\n".encode())

    def status(self, conn, obj_kvs):
        with self.send_lock:
            conn.sendall(f"S{self.handle_hex}|{obj_kvs}\n".encode())

    def on_line(self, conn, line):
        if not line.startswith("C") or "|" not in line:
            log("[<-] (non-C)", line); return
        head, cmd = line.split("|", 1)
        seq, c = head[1:], cmd.strip()
        log("[<-]", f"C{seq}|{c}")
        if c.startswith("client gui"):
            self.reply(conn, seq, str(uuid.uuid4()).upper())
        elif c.startswith("client set"):
            if "send_reduced_bw_dax=1" in c:
                self.audio_reduced_bw = True
            elif "send_reduced_bw_dax=0" in c:
                self.audio_reduced_bw = False
            self.reply(conn, seq)
        elif c == "slice list":
            self.reply(conn, seq, "")
        elif c.startswith("display panafall create") or c.startswith("display pan create"):
            pid = self._new_pan()                          # AE's +RX = a NEW stacked panadapter
            self.reply(conn, seq, f"0x{pid:08X},0x{self.pans[pid]['wf_id']:08X}")
            self.emit_pan_status(conn, pid)
        elif c.startswith("display pan remove") or c.startswith("display panafall remove"):
            self.reply(conn, seq)
            for t in c.split():
                if t.lower().startswith("0x"):
                    pid = int(t, 16)
                    if self.pans.pop(pid, None):           # stop streaming the removed panadapter
                        for g, sl in list(self.slices.items()):
                            if sl.get("pan") == pid: self.slices.pop(g, None)
                        log(f"[pan] remove -> 0x{pid:08X}  ({len(self.pans)} left)")
                    break
        elif c.startswith("slice create"):
            kvs = parse_kvs(c)
            idx = self._next_slice_index()
            if idx is None:                                # model's slice budget exhausted
                self.reply(conn, seq, code=0x50000000)     # -> AE: "No available slices"
                log(f"[slice] create refused: all {self.max_slices} slices in use")
                return
            pid = self._pan_from_kvs(kvs)                  # the panadapter this receiver lands on
            freq = float(kvs["freq"]) if "freq" in kvs else self.slice_freq
            mode = kvs.get("mode", self.slice_mode)
            for s in self.slices.values(): s["active"] = False
            self.slices[idx] = {"freq": freq, "mode": mode, "active": True, "pan": pid}
            self.pans[pid]["slice"] = idx
            self.pans[pid]["center"] = freq                # pan frames on its slice at creation
            self.active_slice = idx
            self._sync_active_slice()                      # mirror active slice -> slice_freq/mode + pan centre
            self.reply(conn, seq, str(idx))
            log(f"[slice] create -> slice {idx} @ {freq:.5f} {mode} on pan 0x{pid:08X}  ({len(self.slices)}/{self.max_slices})")
            self.emit_slice_status(conn, idx)
            self.emit_meter_status(conn)                   # (re)define S-meters incl. the new slice
            self.emit_pan_status(conn, pid)                # centre this pan/waterfall on its slice
        elif c.startswith("display pan set"):
            kvs = parse_kvs(c)
            pid = None                                     # which panadapter this set targets
            for t in c.split():
                if t.lower().startswith("0x"):
                    try: pid = int(t, 16)
                    except ValueError: pid = None
                    break
            pan = self.pans.get(pid)
            if "ypixels" in kvs: self.y_pixels = max(2, int(kvs["ypixels"]))
            if "y_pixels" in kvs: self.y_pixels = max(2, int(kvs["y_pixels"]))
            if "min_dbm" in kvs: self.min_dbm = float(kvs["min_dbm"])
            if "max_dbm" in kvs: self.max_dbm = float(kvs["max_dbm"])
            if "bandwidth" in kvs: self.span_mhz = float(kvs["bandwidth"])
            # Retune THIS panadapter by absolute centre= or by band= (AE's band buttons).
            # Per-pan, not radio-global, so stacked pans can sit on different bands; move
            # the pan's slice with it (a band change retunes its receiver, like a real radio).
            new_center = None
            if "band" in kvs:
                bc = BAND_CENTERS_MHZ.get(str(kvs["band"]).strip().lower())
                if bc is not None: new_center = bc
            if "center" in kvs:
                try: new_center = float(kvs["center"])
                except ValueError: pass
            if new_center is not None:
                if pan is not None:
                    pan["center"] = new_center
                    sidx = pan.get("slice")
                    if sidx is not None and sidx in self.slices:
                        self.slices[sidx]["freq"] = new_center
                else:
                    self.center_mhz = new_center           # no pan id: legacy radio-global behaviour
                self._sync_active_slice()
            self.reply(conn, seq)
            if pan is not None and new_center is not None:
                self.emit_pan_status(conn, pid)            # re-announce the pan's new centre to AE
                sidx = pan.get("slice")
                if sidx is not None and sidx in self.slices:
                    self.emit_slice_status(conn, sidx)
        elif c == "sub radio all":
            self.reply(conn, seq)
            self.emit_radio_status(conn)                   # slice/pan capability -> AE enables +RX
        elif c.startswith("info"):
            self.reply(conn, seq, f"model={self.model},chassis_serial={self.serial},callsign=SDRSIM,"
                                  f"name=flex-sim,software_ver={VERSION},ip={self.ip}")
        elif c == "mic list":
            self.reply(conn, seq, "MIC,LINE")
        elif c.startswith("client udpport"):
            # AE announces the UDP port it listens on for VITA data. On a single
            # host (AE owns :4992), AE's prime packet to our advertised port may
            # never arrive — so seed vita_dest directly from this announcement +
            # the known peer IP, instead of relying solely on prime_loop. The
            # prime path still works as a fallback (e.g. WSL2 separate stacks).
            try:
                up = int(c.split()[-1])
                if self.ae_peer_ip and not self.vita_dest:
                    self.vita_dest = (self.ae_peer_ip, up)
                    log(f"[udp] VITA dest from client udpport: {self.vita_dest}")
                else:
                    log("[tcp] AE udpport =", up)
            except ValueError:
                pass
            self.reply(conn, seq)
        elif c.startswith("stream create"):
            kvs = parse_kvs(c)
            if kvs.get("type") == "remote_audio_rx":
                sid = AUDIO_SID_BASE + self.radio_id
                self.audio_stream_id = sid
                self.dax_channel = None
                self.reply(conn, seq, f"0x{sid:08X}")
                self._start_audio_thread()
            elif kvs.get("type") == "dax_rx":
                # AE arms a DAX RX channel for RADE / digital decode. It relies on a
                # 'stream <id> type=dax_rx dax_channel=N client_handle=<ours>' STATUS line
                # (TciServer::statusReceived -> registerDaxStream) to learn the id; the
                # command reply alone is NOT enough. Stream the golden clip on this id with
                # the same PCC_IF_NARROW format -> AE routes it to feedRxAudio -> RADE decode.
                try:
                    ch = int(kvs.get("dax_channel", "1"))
                except ValueError:
                    ch = 1
                ch = max(1, min(4, ch))
                sid = DAX_SID_BASE + self.radio_id * 4 + (ch - 1)
                self.audio_stream_id = sid
                self.dax_channel = ch
                self.reply(conn, seq, f"0x{sid:08X}")
                # The registration status line — must carry our client_handle so AE's
                # streamStatusBelongsToUs() accepts it, and the dax_channel it asked for.
                self.status(conn, f"stream 0x{sid:08X} type=dax_rx dax_channel={ch} "
                                  f"client_handle=0x{self.handle_hex}")
                log(f"[dax] registered dax_rx stream 0x{sid:08X} channel={ch}")
                self._start_audio_thread()
            else:
                self.reply(conn, seq, "0x48000000")
        elif c.startswith("stream remove"):
            for tok in c.split():
                if tok.lower().startswith("0x"):
                    if int(tok, 16) == self.audio_stream_id:
                        self.audio_stop.set()
                        self.audio_stream_id = None
                        self.dax_channel = None
            self.reply(conn, seq)
        elif c.startswith("transmit set"):                 # AE keys TX (MOX / TUNE)
            kvs = parse_kvs(c)
            if "mox" in kvs:  self.tx_mox = kvs["mox"] == "1"
            if "tune" in kvs: self.tx_tune = kvs["tune"] == "1"
            self.reply(conn, seq)
            self.emit_transmit_status()
        elif c.startswith("cwx "):                          # AE's CW text keyer
            self.handle_cwx(c)
            self.reply(conn, seq)
        elif c.startswith("slice set"):
            kvs = parse_kvs(c)
            idx = self._slice_index_from(c)
            if idx in self.slices:
                if "audio_mute" in kvs:
                    self.slices[idx]["muted"] = (kvs["audio_mute"] == "1")
                    log(f"[slice] {idx} audio_mute={kvs['audio_mute']}")
                if "mode" in kvs:
                    self.slices[idx]["mode"] = kvs["mode"]
                    if self.slices[idx].get("active"):
                        self.slice_mode = kvs["mode"]
            self.reply(conn, seq)
        elif c.startswith("slice remove"):
            idx = self._slice_index_from(c)
            gone = self.slices.pop(idx, None)
            if gone:                                       # free its panadapter slot too
                self.pans.pop(gone.get("pan"), None)
            self.reply(conn, seq)
            self.status(conn, f"slice {idx} in_use=0")
            log(f"[slice] remove -> slice {idx}  ({len(self.slices)}/{self.max_slices})")
            if idx == self.active_slice and self.slices:
                self.active_slice = next(iter(self.slices))
            self._sync_active_slice()
            self.emit_meter_status(conn)                   # drop the removed slice's S-meter def
        elif c.startswith("slice set") or c.startswith("slice tune"):
            kvs = parse_kvs(c)
            idx = self._slice_index_from(c)
            sl = self.slices.setdefault(idx, {"freq": self.slice_freq, "mode": self.slice_mode,
                                              "active": False, "pan": self._primary_pan()})
            if "mode" in kvs: sl["mode"] = kvs["mode"]
            for k in ("RF_frequency", "freq"):
                if k in kvs: sl["freq"] = float(kvs[k])
            if c.startswith("slice tune"):                 # "slice tune <idx> <freq>" (positional)
                parts = c.split()
                if len(parts) >= 4:
                    try: sl["freq"] = float(parts[3])
                    except ValueError: pass
            if kvs.get("active") == "1":
                for s in self.slices.values(): s["active"] = False
                sl["active"] = True
                self.active_slice = idx
            if kvs.get("autopan") == "1":                  # Pan Lock on: the pan re-centres on the slice
                pid = sl.get("pan")
                if pid in self.pans: self.pans[pid]["center"] = sl["freq"]
            self.reply(conn, seq)
            self._sync_active_slice()
            self.emit_slice_status(conn, idx)
        else:
            self.reply(conn, seq)

    def emit_pan_status(self, conn, pid=None):
        if pid is None: pid = self._primary_pan()
        pan = self.pans.get(pid)
        if not pan: return
        wid, cfreq = pan["wf_id"], self._pan_center(pid)
        self.status(conn,
            f"display pan 0x{pid:08X} client_handle=0x{self.handle_hex} waterfall=0x{wid:08X} "
            f"center={cfreq:.6f} bandwidth={self.span_mhz:.6f} min_dbm={self.min_dbm:.0f} "
            f"max_dbm={self.max_dbm:.0f} x_pixels={self.bins} y_pixels={self.y_pixels} fps={self.fps} "
            f"ant_list=ANT1 rxant=ANT1")
        self.status(conn,
            f"display waterfall 0x{wid:08X} client_handle=0x{self.handle_hex} panadapter=0x{pid:08X} "
            f"line_duration=100 center={cfreq:.6f} bandwidth={self.span_mhz:.6f} "
            f"auto_black=1 black_level=15 color_gain=50")
        log(f"[->] pan 0x{pid:08X} / wf 0x{wid:08X} status @ {cfreq:.5f}")
        if not self.streaming:
            self.streaming = True
            threading.Thread(target=self.stream_loop, daemon=True).start()

    def _new_pan(self):
        pid, wid = self.pan_id + self.pan_seq, self.wf_id + self.pan_seq
        self.pan_seq += 1
        self.pans[pid] = {"wf_id": wid, "slice": None, "y_pixels": self.y_pixels,
                          "center": self.slice_freq}     # framing centre; fixed unless AE pans (autopan)
        log(f"[pan] create -> 0x{pid:08X}/0x{wid:08X}  ({len(self.pans)} panadapter(s))")
        return pid

    def _primary_pan(self):
        return next(iter(self.pans)) if self.pans else self._new_pan()

    def _pan_center(self, pid):                            # a pan's own framing centre (NOT the slice freq:
        pan = self.pans.get(pid)                           # a slice can tune within a fixed pan, autopan=0)
        if pan and "center" in pan:
            return pan["center"]
        return self.slice_freq

    def _pan_from_kvs(self, kvs):                          # resolve "pan=0x...." to one of our panadapters
        if "pan" in kvs:
            try:
                pid = int(str(kvs["pan"]).lower().replace("0x", ""), 16)
                if pid in self.pans: return pid
            except ValueError: pass
        return self._primary_pan()

    def _next_slice_index(self):
        for k in range(self.max_slices):
            if k not in self.slices: return k
        return None                                        # budget exhausted

    def _slice_index_from(self, c):
        for t in c.split()[2:]:                            # after "slice set"/"slice tune"/"slice remove"
            if t.isdigit(): return int(t)
        return self.active_slice

    def _sync_active_slice(self):
        sl = self.slices.get(self.active_slice)
        if sl:
            self.slice_freq, self.slice_mode = sl["freq"], sl["mode"]
            self.center_mhz = self.slice_freq              # active slice drives the pan/waterfall centre

    def emit_slice_status(self, conn, idx=None):
        if idx is None: idx = self.active_slice
        sl = self.slices.get(idx)
        if not sl: return
        pid = sl.get("pan") or self._primary_pan()
        self.status(conn, f"slice {idx} client_handle=0x{self.handle_hex} pan=0x{pid:08X} "
                          f"RF_frequency={sl['freq']:.6f} mode={sl['mode']} in_use=1 "
                          f"active={1 if sl['active'] else 0}")

    def emit_radio_status(self, conn):
        # Capability advert. AE computes MaxSlices/SlicesRemaining from the radio status;
        # without it SlicesRemaining stays 0 and +RX is greyed. slices/max_slices = the model
        # cap; AE stacks one panadapter per receiver, so panadapters tracks the same cap.
        # slices = CURRENT in-use count (AE: free = max_slices - slices); max_slices = the cap.
        # (Sending slices=max told AE "all slices used" -> it refused every new receiver.)
        self.status(conn, f"radio slices={len(self.slices)} max_slices={self.max_slices} "
                          f"panadapters={self.max_slices} "
                          f"lineout_gain=50 lineout_mute=0 headphone_gain=50 headphone_mute=0 "
                          f"callsign=SDRSIM nickname=flex-sim")
        log(f"[->] radio status: slices(in-use)={len(self.slices)} max_slices={self.max_slices}")

    def emit_meter_status(self, conn):
        # Define the radio's meters. NB: meter status is '#'-separated
        # index.key=value tokens after "meter " (NOT space-separated, and NO
        # leading "meter N") -- RadioModel::handleMeterStatus.
        defs = [(FWDPWR_ID, 0, "TX", "FWDPWR", "dBm", 0.0, 60.0),    # TX fwd power (dBm -> W)
                (SWR_ID,    0, "TX", "SWR",    "SWR", 1.0, 10.0)]    # TX SWR
        for k in (sorted(self.slices) or [0]):                       # one S-meter per slice (num = slice index)
            defs.append((SLICE_METER_BASE + k, k, "SLC", "LEVEL", "dBm", -150.0, 20.0))
        for i, num, src, nam, unit, lo, hi in defs:
            toks = "#".join([f"{i}.src={src}", f"{i}.num={num}", f"{i}.nam={nam}",
                             f"{i}.unit={unit}", f"{i}.low={lo}", f"{i}.hi={hi}"])
            self.status(conn, f"meter {toks}")
        log(f"[->] emitted meter defs: {len(self.slices) or 1} S-meter(s) + TX FWDPWR/SWR")

    def emit_transmit_status(self):
        # interlock state drives AE's m_radioTransmitting; transmit status drives
        # the MOX/TUNE UI. tx_client_handle = our handle => AE sees us as TX owner.
        if not self.conn:
            return
        on = self.tx_on
        try:
            self.status(self.conn,
                f"interlock state={'TRANSMITTING' if on else 'READY'} source=SW "
                f"tx_client_handle=0x{self.handle_hex}")
            self.status(self.conn,
                f"transmit mox={1 if self.tx_mox else 0} tune={1 if self.tx_tune else 0} "
                f"freq={self.slice_freq:.6f} rfpower={int(self.tx_power_w)} "
                f"tunepower={int(self.tx_power_w)}")
            log(f"[->] TX {'ON' if on else 'off'}  ({self.tx_power_w:.0f} W, SWR {self.tx_swr:.1f})")
        except OSError:
            pass

    def emit_cwx_status(self, **kv):
        if not self.conn:
            return
        body = " ".join(f"{k}={v}" for k, v in kv.items())
        try:
            self.status(self.conn, f"cwx {body}")
        except OSError:
            pass

    def handle_cwx(self, c):
        rest = c[4:].strip()                               # after "cwx "
        if rest.startswith("wpm"):
            try: self.cwx_wpm = max(5, int(rest.split()[1]))
            except (IndexError, ValueError): pass
        elif rest.startswith("qsk"):                        # cwx qsk_enabled 0/1
            self.qsk = rest.split()[-1] == "1"
            log(f"[cwx] QSK {'on (full break-in)' if self.qsk else 'off (normal)'}")
        elif rest.startswith("send"):                      # cwx send "<enc>" <block>
            q1 = c.find('"'); q2 = c.find('"', q1 + 1)
            if q1 >= 0 and q2 > q1:
                text = c[q1 + 1:q2].replace("\x7f", " ")   # 0x7f = AE-encoded space
                self.start_cwx(text)
        elif rest.startswith("clear") or rest.startswith("erase"):
            if self.cwx_active:
                self.cwx_active = False
                if self.tx_mox:
                    self.tx_mox = False
                    self.emit_transmit_status()
                self.emit_cwx_status(queue=0)

    def start_cwx(self, text):
        sched = _morse_schedule(text, self.cwx_wpm)
        if not sched:
            return
        self.cwx_schedule = sched
        self.cwx_char_ends = _morse_char_ends(text, self.cwx_wpm)
        self.cwx_sent = 0
        self.cwx_dur = sum(d for _, d in sched) + CWX_TAIL_S   # tail so AE's local keyer finishes before unkey
        self.cwx_t0 = time.time()
        self.cwx_active = True
        log(f"[cwx] AE sent {text!r} @ {self.cwx_wpm} wpm  ({self.cwx_dur:.1f}s)")

    def _cwx_frame(self, ctx, now):
        # One frame of an AE-initiated CWX transmission. Returns (levels, keyed).
        el = now - self.cwx_t0
        if el >= self.cwx_dur:                             # buffer drained
            self.cwx_active = False
            if self.tx_mox:
                self.tx_mox = False
                self.emit_transmit_status()
            self.emit_cwx_status(queue=0)                  # -> AE releases MOX (#2450)
            log("[cwx] complete")
            return [ctx.floor] * ctx.n, False
        if not self.tx_mox:                                # key the over on the first frame
            self.tx_mox = True
            self.emit_transmit_status()
        keydown = _schedule_keydown(self.cwx_schedule, el)
        ctx.cw_keydown = keydown
        if keydown or not self.qsk:                        # TX burst, or non-QSK over: RX muted
            return None, keydown
        out = [ctx.floor] * ctx.n                           # QSK: RX peeks between elements
        for d in range(-ctx.sig_half, ctx.sig_half + 1):
            b = ctx.center + d
            if 0 <= b < ctx.n:
                out[b] = ctx.sig_level
        return out, keydown

    def _start_audio_thread(self):
        """Start the shared audio_loop if not already running. Used by both the
        remote_audio_rx and dax_rx 'stream create' paths — the loop streams on
        self.audio_stream_id with PCC_IF_NARROW, which AE accepts for either route."""
        if not (self.audio_thread and self.audio_thread.is_alive()):
            self.audio_stop.clear()
            self.audio_thread = threading.Thread(
                target=self.audio_loop, daemon=True, name=f"audio-{self.radio_id}")
            self.audio_thread.start()

    def audio_loop(self):
        """Send remote_audio_rx / dax_rx VITA-49 packets at AUDIO_RATE.

        Three audio sources are supported (self.audio_source):
          AUDIO_SRC_TONE  — deterministic sine at audio_tone_hz (default)
          AUDIO_SRC_NOISE — gaussian band noise
          AUDIO_SRC_WAV   — WAV file at audio_wav_path, resampled + looped
        """
        t0 = time.monotonic()
        while self.run and not self.vita_dest and time.monotonic() - t0 < 12:
            time.sleep(0.05)
        if not self.vita_dest:
            log("[audio] no UDP dest — cannot stream"); return

        s = socket.socket(socket.AF_INET, socket.SOCK_DGRAM)
        dest    = self.vita_dest
        src     = self.audio_source

        # Open WAV file if needed (fall back to tone on error)
        wav = None
        if src == AUDIO_SRC_WAV:
            try:
                wav = WavPlayer(self.audio_wav_path)
            except Exception as e:
                log(f"[audio] WAV open failed ({e}), falling back to tone")
                src = AUDIO_SRC_TONE

        log(f"[audio] streaming [{src}] → {dest}  (stream/format chosen live per route)")
        last_route = None   # log only when the route/format actually changes

        seq      = 0
        sample_t = 0   # phase counter for tone source
        t_start  = time.monotonic()
        amp      = 0.1  # −20 dBFS

        wav_path = self.audio_wav_path        # track which file is open, to detect live changes
        try:
            while self.run and not self.audio_stop.is_set():
                # Re-read the source each iteration so the control panel can hot-swap
                # tone/noise/wav (and the WAV path) live without recreating the stream.
                src = self.audio_source
                # Choose stream id + format LIVE: when a dax_rx (RADE/digital) channel is
                # active, stream on the DAX id and FORCE full-BW float32 stereo — the RADE
                # OFDM modem needs full bandwidth, so ignore send_reduced_bw_dax here. If AE
                # arms RADE after remote_audio_rx already started this loop, this switches the
                # running loop onto the dax stream without recreating it.
                dax_on   = self.dax_channel is not None
                reduced  = False if dax_on else self.audio_reduced_bw
                stream_id = self.audio_stream_id
                route = (f"dax_rx ch{self.dax_channel} (full-BW f32)" if dax_on
                         else f"remote_audio_rx ({'mono int16' if reduced else 'stereo f32'})")
                if route != last_route:
                    log(f"[audio] route -> {route}, sid 0x{stream_id:08X}")
                    last_route = route
                if src == AUDIO_SRC_WAV and (wav is None or self.audio_wav_path != wav_path):
                    try:
                        if wav: wav.close()
                        wav = WavPlayer(self.audio_wav_path)
                        wav_path = self.audio_wav_path
                        log(f"[audio] source -> wav: {wav_path}")
                    except Exception as e:
                        log(f"[audio] WAV open failed ({e}), tone"); src = AUDIO_SRC_TONE; wav = None

                any_muted = any(sl.get("muted") for sl in self.slices.values())
                if self.paused or any_muted:                       # Stop: silence the audio too (not just the
                    mono = [0.0] * AUDIO_FRAMES                     #        spectrum) — keep the stream alive so
                    #                                                Go resumes cleanly with continuous timing.
                elif src == AUDIO_SRC_WAV and wav:
                    mono = wav.read(AUDIO_FRAMES)
                elif src == AUDIO_SRC_NOISE:
                    mono = [random.gauss(0, amp) for _ in range(AUDIO_FRAMES)]
                else:  # AUDIO_SRC_TONE
                    mono = [amp * math.sin(2 * math.pi * self.audio_tone_hz * (sample_t + i) / AUDIO_RATE)
                            for i in range(AUDIO_FRAMES)]

                sample_t += AUDIO_FRAMES  # harmless for non-tone sources

                if reduced:
                    samples = mono
                else:
                    samples = []
                    for v in mono: samples.extend([v, v])

                try:
                    s.sendto(audio_packet(stream_id, seq & 0xF, samples, reduced_bw=reduced), dest)
                except OSError as e:
                    log("[audio] send error:", e); break

                seq += 1
                # Absolute deadline — prevents accumulated drift
                deadline = t_start + seq * AUDIO_INTERVAL
                wait = deadline - time.monotonic()
                if wait > 0:
                    time.sleep(wait)
        finally:
            s.close()
            if wav:
                wav.close()
            log("[audio] stopped")

    def stream_loop(self):
        t0 = time.time()
        while self.run and not self.vita_dest and time.time() - t0 < 12:
            time.sleep(0.1)
        if not self.vita_dest:
            log("[stream] no UDP prime in 12s — AE never opened the data path; cannot stream")
            return
        dest = self.vita_dest
        log(f"[stream] streaming '{self.pattern}' to {dest} @ {self.fps} fps (Ctrl-C to stop)")
        s = socket.socket(socket.AF_INET, socket.SOCK_DGRAM)
        ctx = PatternCtx(self.bins, self.bins // 2, self.min_dbm, self.max_dbm)
        gen = PATTERNS[self.pattern]
        fseq = wseq = mseq = fi = 0
        last_tc = -1                                                # throttle pan/wf to one per row
        start = time.time()
        while self.run and self.streaming:
            now = time.time()
            if self.paused:                                        # Stop: send nothing; AE's pan/wf go dead
                time.sleep(0.1)                                    #       but the radio stays connected. Go resumes.
                continue
            cw_mode = self.cwx_active or self.pattern == "cw"
            period = (1.0 / 50.0) if cw_mode else (1.0 / self.fps)  # 20-ms keying res for 20-wpm CW (60-ms dits)
            tc = int((now - start) * self.fps)                     # waterfall row index (loop-rate independent);
            #                                                        wall-clock so it JUMPS across a TX gap
            #                                                        (real-radio behaviour -> AE history reproj).
            ctx.min_dbm, ctx.max_dbm = self.min_dbm, self.max_dbm   # track AE's display-pan-set
            ctx.floor = self.noise_floor_dbm                        # live (control panel, dBm)
            ctx.sig_level = self.sig_level_dbm
            ctx.noise_color = self.noise_color
            ctx.cw_qsk = self.qsk
            binbw_hz = (self.span_mhz * 1e6) / self.bins            # track AE's live span
            ctx.sig_half = max(1, int(round((self.sig_width_khz * 1000.0) / binbw_hz / 2)))
            ctx.two_tone_half_bins = max(1, int(round((TWO_TONE_SPACING_HZ / binbw_hz) / 2)))  # two-tone ruler spacing in bins
            ctx.noise_cal_full = self.noise_cal_full        # noise_cal: full-span (standard) vs band-limited
            if self.cwx_active:                                    # AE-initiated CWX overrides the pattern
                levels, keyed = self._cwx_frame(ctx, now)
            else:
                gen = PATTERNS.get(self.pattern, gen)              # live pattern switch
                levels = gen(ctx, now - start)
                if self.pattern == "tx_blank":                    # faithful TX cycle: the gap == keyed.
                    want_tx = (levels is None)
                elif self.pattern == "cw":                        # Morse keyer: TX for the whole over
                    want_tx = ctx.cw_in_message
                else:
                    want_tx = None
                if want_tx is not None and want_tx != self.tx_mox:  # assert real TX -> repro #2126/#1916
                    self.tx_mox = want_tx
                    self.emit_transmit_status()
                keyed = ctx.cw_keydown if self.pattern == "cw" else self.tx_on
            if levels is not None and tc != last_tc:               # one pan/wf row per waterfall tick
                pixels = [self.dbm_to_pixel(d) for d in levels]     # generated once; each stacked panadapter
                intens = [self.dbm_to_wf_raw(d) for d in levels]    # shows it, centred on ITS slice (low_hz).
                wf_ab = self.dbm_to_wf_raw(self.noise_floor_dbm)    # auto-black = the CONFIGURED noise floor,
                #   not min(intens): a flat pattern (step/impulse/ramp) has min==max, so min(intens) would
                #   set the black level AT the signal and AE blanks the whole waterfall row. The true floor
                #   keeps flat-high rows bright and still feeds AE's #3586 auto-black path correctly.
                self.last_vfo_dbm = levels[ctx.center]              # active slice (pan centre) -> rack strip
                try:
                    for pid, pan in list(self.pans.items()):        # AE stacks one panadapter per receiver
                        low_hz = (self._pan_center(pid) - self.span_mhz / 2) * 1e6
                        s.sendto(fft_packet(pid, fseq & 0xF, pixels, fi), dest); fseq += 1
                        s.sendto(wf_packet(pan["wf_id"], wseq & 0xF, intens, low_hz, binbw_hz, tc, auto_black=wf_ab), dest); wseq += 1
                    for g in list(self.slices):                     # per-slice S-meter (level at slice = pan centre)
                        s.sendto(meter_packet(self.meter_sid, mseq & 0xF, SLICE_METER_BASE + g, levels[ctx.center]), dest); mseq += 1
                except OSError as e:
                    log("[stream] send error:", e); break
                fi += 1
                last_tc = tc
            # TX meters every frame: real power/SWR while keyed, ~0 W / 1.0 SWR when not,
            # so AE's meter decays back to zero on de-key. CW/CWX key power per element.
            pw = self.tx_power_w if keyed else 0.0
            sw = self.tx_swr if keyed else 1.0
            fwd_dbm = 10.0 * math.log10(max(pw, 1e-6)) + 30.0
            try:
                s.sendto(meter_packet(self.meter_sid, mseq & 0xF, FWDPWR_ID, fwd_dbm), dest); mseq += 1
                s.sendto(meter_packet(self.meter_sid, mseq & 0xF, SWR_ID, sw), dest); mseq += 1
            except OSError:
                pass
            dt = period - (time.time() - now)
            if dt > 0: time.sleep(dt)
        log("[stream] stopped")


